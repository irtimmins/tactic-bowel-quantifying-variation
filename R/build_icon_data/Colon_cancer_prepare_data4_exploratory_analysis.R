                         labels = c("<50", "50-59", "60-69", "70-79", "80+"),
                         right  = TRUE)
  )


# Winsorise at 99th percentile to reduce distortion from extreme outliers
#cap99 <- function(x) pmin(x, quantile(x, 0.99, na.rm = TRUE))
df <- df %>%
  mutate(across(c(wt_dx_to_tx, wt_dx_to_dtt, wt_dtt_to_tx), cap99))

cat("Analytic cohort n =", nrow(df), "\n")


# ?????? 1. OVERALL SUMMARY ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

wt_vars  <- c("wt_dx_to_tx", "wt_dx_to_dtt", "wt_dtt_to_tx")
wt_labels <- c("Dx ??? Treatment", "Dx ??? DTT", "DTT ??? Treatment")

overall_summary <- df %>%
  summarise(across(all_of(wt_vars), list(
    n      = ~sum(!is.na(.)),
    median = ~median(., na.rm = TRUE),
    q25    = ~quantile(., 0.25, na.rm = TRUE),
    q75    = ~quantile(., 0.75, na.rm = TRUE),
    mean   = ~mean(., na.rm = TRUE),
    sd     = ~sd(., na.rm = TRUE)
  ))) %>%
  pivot_longer(everything(), names_to = c("variable", ".value"), names_sep = "_(?=[^_]+$)") %>%
  mutate(variable = recode(variable, !!!setNames(wt_labels, wt_vars)))

print(overall_summary)


# ?????? Helper: grouped summary ?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

grouped_summary <- function(data, group_var) {
  data %>%
    group_by({{ group_var }}) %>%
    summarise(across(all_of(wt_vars), list(
      n      = ~sum(!is.na(.)),
      median = ~median(., na.rm = TRUE),
      q25    = ~quantile(., 0.25, na.rm = TRUE),
      q75    = ~quantile(., 0.75, na.rm = TRUE)
    )), .groups = "drop")
}

# ?????? Helper: violin + box plot ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

violin_box <- function(data, x_var, y_var, x_label, y_label, fill_pal = NULL) {
  p <- ggplot(data %>% filter(!is.na({{ x_var }})),
              aes(x = {{ x_var }}, y = {{ y_var }}, fill = {{ x_var }})) +
    geom_violin(alpha = 0.45, colour = NA, trim = TRUE) +
    geom_boxplot(width = 0.18, outlier.shape = NA, colour = "grey20", alpha = 0.7) +
    stat_summary(fun = median, geom = "point", shape = 18, size = 2.5, colour = "white") +
    labs(x = x_label, y = y_label) +
    theme_minimal(base_size = 11) +
    theme(legend.position = "none",
          panel.grid.major.x = element_blank(),
          axis.text.x = element_text(angle = 20, hjust = 1))
  if (!is.null(fill_pal)) p <- p + scale_fill_manual(values = fill_pal)
  p
}


